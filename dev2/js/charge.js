function TRViewLess1(){
				$('#more1BTN').html('View More');
				$('#more1BTN').attr("onClick","TRViewMore1()");
				$('#more1').fadeOut(1000);

}

function TRViewMore1(){
				$('#more1').css("diplay", "block");
                $('#more1').fadeIn(1000);
				$('#more1BTN').html('View Less');
				$('#more1BTN').attr("onClick","TRViewLess1()");
}

